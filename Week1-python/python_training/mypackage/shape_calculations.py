""" Area, Circumference, of SQ,RECT,CIRCLE """

from math import pi

def area_of_circle(side):
    """
    Area of Square
    :param side: side of square
    :return: Area
    """
    return side*side

def area_of_circle(rad):
    """
    Area of Circle
    :param rad: radius of circle
    :return:
    """
    return pi*rad*rad

def area_of_react(len,brd):
    """
    Area of React
    :param len: length of react
    :param brd: breadth of react
    :return: length of breadth of react
    """
    return len*brd

def cir_of_circle(rad):
    return 2*pi*rad
